
package Question1;


public class Hospital {
    
    private String name;
    private String hospital;
    private String years;

    public Hospital() {
    }

    public Hospital(String name, String hospital, String years) {
        this.name = name;
        this.hospital = hospital;
        this.years = years;
    }

    public String getName() {
        return name;
    }

    public String getHospital() {
        return hospital;
    }

    public String getYears() {
        return years;
    }
  
}
