    public void save()
    {
        if(txtHospital.getText().isEmpty() || txtHospital.getText().equals("") || txtHospital.getText()==null)
        {
            JOptionPane.showMessageDialog(null,"Enter Hospital Name","Error",JOptionPane.ERROR_MESSAGE);
        }
        if(txtLocation.getText().isEmpty() || txtLocation.getText().equals("") || txtLocation.getText()==null)
        {
            JOptionPane.showMessageDialog(null,"Enter Location","Error",JOptionPane.ERROR_MESSAGE);
        }
        
        
        if( !txtHospital.getText().isEmpty() || !txtHospital.getText().equals("") || txtHospital.getText()!=null 
                && !txtLocation.getText().isEmpty() || !txtLocation.getText().equals("") || txtLocation.getText()!=null)
        {
           
                name=txtHospital.getText().trim();
                location=txtLocation.getText().trim();
                years=cmbYears.getSelectedItem().toString();
                
                data.add(new Hospital(name,location,years));
                
                JOptionPane.showMessageDialog(rootPane,"Information Is Saved");
                txtHospital.setText("");
                txtLocation.setText(""); 
                
                if(data.size()>5)
                {
                   JOptionPane.showMessageDialog(rootPane,"Maximum inspection count reached","Meesage",JOptionPane.ERROR_MESSAGE); 
                }

            

            
        }
    }
