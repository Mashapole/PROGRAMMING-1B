    public void print()
    {
        if(data.size()>0)
        {
        //display data
        for(Hospital i: data)
        {
            val+="Hospital Name:  " +i.getName() + "\nHospital Location:    " + i.getHospital() + "\nYears Since Inspection:   " + i.getYears()+"\n\n";
        }
        
        txtOutput.setText(val+end); 
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Display Is Empty","Meesage",JOptionPane.ERROR_MESSAGE); 
        }

    }
